  static String of(BuildContext context, String key) {
    final lang = Localizations.localeOf(context).languageCode;
    final map = _t[lang] ?? _t['en']!;
    return map[key] ?? _t['en']![key] ?? key;
  }

  /// بدون [BuildContext] — للطبقة المنطقية (مثل المحفظة).
  static String byLang(String languageCode, String key) {
    final map = _t[languageCode] ?? _t['en']!;
    return map[key] ?? _t['en']![key] ?? key;
  }

  static String format(
    BuildContext context,
    String key, {
    Map<String, String> params = const {},
  }) {
    var s = of(context, key);
    params.forEach((k, v) {
      s = s.replaceAll('{$k}', v);
    });
    return s;
  }

  static String formatLang(
    String languageCode,
    String key, {
    Map<String, String> params = const {},
  }) {
    var s = byLang(languageCode, key);
    params.forEach((k, v) {
      s = s.replaceAll('{$k}', v);
    });
    return s;
  }
